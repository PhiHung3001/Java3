    public void loadDataToTable() {
        try {
            String sql = "Select * from SINHVIEN";
            Statement pstm = cn.createStatement();
            ResultSet rs = pstm.executeQuery(sql);
            model.setRowCount(0);
            while (rs.next()) {
                String masv = rs.getString(1);
                String tensv = rs.getString(2);
                int tuoi = rs.getInt(3);
                String nganh = rs.getString(4);

                model.addRow(new Object[]{masv, tensv, tuoi, nganh});

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    public void Display(int i) throws SQLException {
        
        
        txtMa.setText(tblShow.getValueAt(i, 0).toString());
        txtTen.setText(tblShow.getValueAt(i, 1).toString());
        txtTuoi.setText(tblShow.getValueAt(i, 2).toString());
        cboNganh.setSelectedItem(tblShow.getValueAt(i, 3).toString());
        

    }

