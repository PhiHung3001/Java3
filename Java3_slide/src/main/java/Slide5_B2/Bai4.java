    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:

        try {
            String maSV = txtMa.getText();
            String tenSV = txtTen.getText();
            int tuoi = Integer.parseInt(txtTuoi.getText());
            String nganh = cboNganh.getSelectedItem().toString();
            String url = "insert into SINHVIEN values(?,?,?,?)";
            PreparedStatement ps = cn.prepareStatement(url);
            ps.setString(1, maSV);
            ps.setString(2, tenSV);
            ps.setInt(3, tuoi);
            ps.setString(4, nganh);
            model.addRow(new Object[]{maSV, tenSV, tuoi, nganh});
            int row = ps.executeUpdate();
            if (row > 0) {
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                //Xử lý sau khi thêm

                Display(i);

                loadDataToTable();
            } else {
                JOptionPane.showMessageDialog(this, "Không thêm được dòng nào");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Bai4.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnThemActionPerformed

