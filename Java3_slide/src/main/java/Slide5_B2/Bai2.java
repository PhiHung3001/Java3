    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
            String maHS = txtMa.getText();
            String tenHS = txtTen.getText();
            double diem = Double.parseDouble(txtDiem.getText());
//            String gioiTinh;
//            if(rdoNam.isSelected()){
//                gioiTinh="Nam";
//            }else{
//                gioiTinh="Nữ";
//            }
            String gioiTinh = rdoNam.isSelected() ? "Nam" : "Nữ";
            list.add(new Student(maHS, tenHS, gioiTinh, diem));
            String sql = "insert into HOCSINH(MaHS,TenHS,GioiTinh,DiemTB)\n"
                    + "values(?,?,?,?)";
            PreparedStatement pstm = cn.prepareStatement(sql);
            pstm.setString(1, maHS);
            pstm.setString(2, tenHS);
            pstm.setString(3, gioiTinh);
            pstm.setDouble(4, diem);
            int row = pstm.executeUpdate();
            if (row > 0) {
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                //Xử lý sau khi thêm
                index = list.size() - 1;
                showDetail(index);

                LoadDataToList();
            } else {
                JOptionPane.showMessageDialog(this, "Không thêm được dòng nào");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if (txtMa.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Nhập mã MaHS");
            txtMa.requestFocus();
            return;

        }
        try {

            String sql = "update HOCSINH set TenHS=?,GioiTinh=?,DiemTB=? where MaHS=?";
            PreparedStatement st = cn.prepareStatement(sql);
            st.setString(4, txtMa.getText());
            st.setString(1, txtTen.getText());

            st.setString(3, txtDiem.getText());
            boolean gt;
            if (rdoNam.isSelected()) {
                gt = true;
            } else {
                gt = false;
            }
            st.setBoolean(2, gt);

            st.executeUpdate();
            JOptionPane.showMessageDialog(this, "Update thành công");
            cn.close();
            LoadDataToList();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (txtMa.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Nhập MaHS");
            txtMa.requestFocus();
            return;

        }

        try {

            String sql = "delete from HOCSINH where MaHS =?";
            PreparedStatement st = cn.prepareStatement(sql);
            st.setString(1, txtMa.getText());
            st.executeUpdate();
            JOptionPane.showMessageDialog(this, "Delete thành công");
            
            list.removeAll(list);
            st.close();
            LoadDataToList();
            showDetail(index);

            

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

