    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            try {
                if (txtMa.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Mã không được bỏ trống");
                    return;
                }
                String sql = "select Masach from SACH";
                Statement st = cn.createStatement();
                ResultSet rs = st.executeQuery(sql);
                while (rs.next()) {
                    if (txtMa.getText().equals(rs.getString(1))) {
                        JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
                        return;
                    }

                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            try {
                if (txtTen.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Tên không được bỏ trống");
                    return;
                }
            } catch (Exception e) {
            }
            try {
                if (txtNXB.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "NXB không được bỏ trống");
                    return;
                }
            } catch (Exception e) {
            }

            try {
                if (txtSoLuong.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Số lượng không được bỏ trống");
                    return;
                } else if (Integer.parseInt(txtSoLuong.getText()) <= 0) {
                    JOptionPane.showMessageDialog(this, "Số lượng phải lớn hơn 0");
                    return;
                }
            } catch (Exception e) {
                  JOptionPane.showMessageDialog(this, "Số lượng phải là số");
                    return;
            }
            try {
                
                if (txtGiaTien.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Giá tiền không được bỏ trống");
                    return;
                }
                if (Double.parseDouble(txtGiaTien.getText()) <= 0) {
                    JOptionPane.showMessageDialog(this, "Giá tiền phải lơn hơn 0");
                    return;
                }
            } catch (Exception e) {
                  JOptionPane.showMessageDialog(this, "Giá tiền phải là số");
                    return;
            }

            String sql = "insert into SACH(Masach,TenSach,MaTheLoai,NXB,SoLuong,Giatien) values(?,?,?,?,?,?)";
            PreparedStatement ps = cn.prepareStatement(sql);
            ps.setString(1, txtMa.getText());
            ps.setString(2, txtTen.getText());
            ps.setString(3, cboTheLoai.getSelectedItem().toString());
            ps.setString(4, txtNXB.getText());
            ps.setString(5, txtSoLuong.getText());
            ps.setString(6, txtGiaTien.getText());
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "thêm thành công");
            ps.close();
            fillTable();
            
        } catch (Exception e) {
            e.printStackTrace();

        }

    }//GEN-LAST:event_btnSaveActionPerformed

