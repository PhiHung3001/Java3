    private void fillCBO() {
        try {
            String sql = "select MaTheLoai from THELOAI";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                cboTheLoai.addItem(rs.getString(1));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void fillTable() {

        try {
            model.setRowCount(0);
            String sql = "select Masach,TenSach,SACH.MaTheLoai,NXB,SoLuong,Giatien from SACH join THELOAI on "
                    + "SACH.MaTheLoai = THELOAI.MaTheLoai where THELOAI.MaTheLoai = '" + cboTheLoai.getSelectedItem().toString().trim() + "'";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6)});
            }
        } catch (Exception e) {
            e.printStackTrace();
            
        }

    }

    private void Display(int index) {
        index = tblShow.getSelectedRow();
        txtMa.setText(tblShow.getValueAt(index, 0).toString());
        txtTen.setText(tblShow.getValueAt(index, 1).toString());
        txtNXB.setText(tblShow.getValueAt(index, 3).toString());
        txtSoLuong.setText(tblShow.getValueAt(index, 4).toString());
        txtGiaTien.setText(tblShow.getValueAt(index, 5).toString());

    }

